type RecordOptions = {
  laureateIds?: string[];
  citation?: string;
  notes?: string;
  officialCitationUrl?: string;
};

function r(
  id: string,
  awardId: string,
  year: number,
  recipientNames: string[],
  sourceIds: string[],
  completenessStatus: CompletenessStatus,
  options: RecordOptions = {},
): WinnerRecord {
  const officialCitationUrl =
    options.officialCitationUrl ?? sourceUrls[sourceIds[0]];
  const record: WinnerRecord = {
    id,
    awardId,
    year,
    recipientNames,
    sourceIds,
    completenessStatus,
  };
  if (options.laureateIds) record.laureateIds = options.laureateIds;
  if (options.citation) record.citation = options.citation;
  if (officialCitationUrl) record.officialCitationUrl = officialCitationUrl;
  if (options.notes) record.notes = options.notes;
  return record;
}

