function profile(
  id: string,
  name: string,
  fields: string[],
  institution: string,
  institutionCountry: string,
  awards: Array<[string, number, string[]]>,
  contribution: string,
  sourcesForProfile: string[],
  status: Laureate["completenessStatus"] = "in-progress",
): ProfileSeed {
  return {
    id,
    name,
    fields,
    institution,
    institutionCountry,
    awards: awards.map(([awardId, year, sourceIds]) => ({
      awardId,
      year,
      sourceIds,
    })),
    contribution,
    sources: sourcesForProfile,
    status,
  };
}

